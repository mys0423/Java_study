package castingTask;

public class CastingTask03 {
   public static void main(String[] args) {
//      'a', 'j', 'k'를 연산하여 'A', 'J', 'K' 출력하기
	   char atoA = 'a' - 32;
	   char jtoJ = 'j' - 32;
	   char ktoK = 'k' - 32;
	   
	   System.out.println(atoA);
	   System.out.println(jtoJ);
	   System.out.println(ktoK);
   }
}